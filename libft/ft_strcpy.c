#include "libft.h"

char	*ft_strcpy(char *dst, const char *src)
{
	char *mem_dst;

	mem_dst = dst;
	while (*src != '\0')
	{
		*dst = *src;
		src++;
		dst++;
	}
	*dst = '\0';
	return (mem_dst);
}
