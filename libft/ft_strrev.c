#include "libft.h"

void	ft_strrev(char *str)
{
	int	i;
	int	len;

	len = ft_strlen(str);
	i = 0;
	while (i < (len / 2))
	{
		ft_swap((str + i), (str + len - i - 1), sizeof(char));
		i++;
	}
}
